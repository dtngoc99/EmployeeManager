    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        if(checkExist){
            if(checkOfficial){
                for(OfficialEmployee o:man.officialList){
                    if(official.employeeID==o.employeeID){
                        o=official;
                        try {
                            man.writeFileOfficialEmloyee();
                        } catch (IOException ex) {
                            Logger.getLogger(EmployeeJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    }
                }
            }
            if(checkParttime){
                for(ParttimeEmployee p: man.parttimeList){
                    if(parttime.employeeID==p.employeeID){
                        p=parttime;
                        try {
                            man.writeFileParttimeEmployee();
                        } catch (IOException ex) {
                            Logger.getLogger(EmployeeJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        break;
                    }
                }
            }
            
        }
        setToDefaul();
        // TODO add your handling code here:
    }//GEN-LAST:event_btUpdateActionPerformed

