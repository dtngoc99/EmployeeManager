    private void tfIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfIDActionPerformed
        try {
            man.readFileOfficialEmployee();
            man.readFileParttimeEmployee();
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(EmployeeJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        emp.employeeID=Integer.parseInt(tfID.getText());
        for(OfficialEmployee o: man.officialList){
            if(emp.employeeID==o.employeeID)
            {
                emp=o;
                official=o;
                tfName.setText(emp.employeeName);
                tfBirthday.setText(emp.birthday);
                tfAddress.setText(emp.address);
                tfPhone.setText(emp.phoneNumber);
                tfSalary.setText(String.valueOf(emp.salary));
                cbOfficial.setSelected(true);
                if("Nu".equals(emp.gender)){
                    cbGirl.setSelected(true);
                }
                else cbBoy.setSelected(true);
                checkExist=true;
                checkOfficial=true;
                break;
            }
        }
        if(!checkExist){
            for(ParttimeEmployee p:man.parttimeList){
                if(emp.employeeID==p.employeeID){
                    emp=p;
                    parttime=p;
                    checkParttime=true;
                    checkExist=true;
                    tfName.setText(emp.employeeName);
                    tfBirthday.setText(emp.birthday);
                    tfAddress.setText(emp.address);
                    tfPhone.setText(emp.phoneNumber);
                    tfSalary.setText(String.valueOf(emp.salary));
                    cbParttime.setSelected(true);
                    if("Nu".equals(emp.gender)){
                        cbGirl.setSelected(true);
                    }
                    else cbBoy.setSelected(true);   
                    break;
                }
            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tfIDActionPerformed

